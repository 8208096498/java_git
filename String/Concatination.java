public class Concatination {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		String a="Eagle";
		String b="Vision";
		int c =100;
	
		System.out.println(a+""+b);
		System.out.println(a+""+c);
		System.out.println(c+""+b);
	}

}
